#include <iostream>
using namespace std;

int main(){
	int a;
	char b;
	float c;
	double d;
	bool e;
	cout << "int: " << sizeof(a) << endl;
	cout << "char: " << sizeof(b) << endl;
	cout << "float: " << sizeof(c) << endl;
	cout << "double: " << sizeof(d) << endl;
	cout << "bool: " << sizeof(e) << endl;
	return 0;
}